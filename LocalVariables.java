import java.util.*;

public class LocalVariables {
    public void sum() {
        // Local variable to sum function
        int x = 25;
        int y = 35;

        int z = x + y;

        System.out.println("Sum: " + z);
    }

    public static void main(String[] args) {
        // Local variables
        int a = 10;
        int b = 20;

        int c = a + b;

        // Print the sum of a and b
        System.out.println("Sum: " + c);
    }
}